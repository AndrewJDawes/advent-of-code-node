import { expect } from 'chai';
import C1 from './C1.js';
describe('Input Transformer ColumnToRow C1', () => {
    it('Buffers and transforms input', () => {
        const input = [
            ['4', '21', '894'],
            ['419', '794', '987'],
            ['424', '797', '125'],
            ['651', '305', '558'],
            ['655', '631', '963'],
            ['2', '628', '436'],
            ['736', '50', '363'],
            ['657', '707', '408'],
            ['252', '705', '98'],
            ['532', '173', '878'],
            ['574', '792', '854'],
            ['157', '737', '303'],
            ['468', '76', '580'],
            ['502', '503', '434'],
        ];
        const c1 = new C1(3);
        for (const row of input) {
            c1.write(row);
        }
        expect(c1.read()).to.deep.equal([
            ['4', '419', '424'],
            ['21', '794', '797'],
            ['894', '987', '125'],
            ['651', '655', '2'],
            ['305', '631', '628'],
            ['558', '963', '436'],
            ['736', '657', '252'],
            ['50', '707', '705'],
            ['363', '408', '98'],
            ['532', '574', '157'],
            ['173', '792', '737'],
            ['878', '854', '303'],
        ]);
        expect(c1.read()).to.deep.equal([]);
    });
});
